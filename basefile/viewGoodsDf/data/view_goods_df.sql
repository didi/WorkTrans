  INDEX `idx_cid_did_goods_shike`(`cid`, `did`, `goods_bid`, `shike`) USING BTREE
) ENGINE = InnoDB AUTO_INCREMENT = 94 CHARACTER SET = utf8mb4 COLLATE = utf8mb4_croatian_ci ROW_FORMAT = Dynamic;