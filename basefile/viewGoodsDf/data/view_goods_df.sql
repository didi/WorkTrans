  `adjust_gmv` double NULL DEFAULT NULL COMMENT '手工修改',
  `true_orders` int(11) NULL DEFAULT NULL COMMENT '真实订单量',
  `predict_orders` int(11) NULL DEFAULT NULL COMMENT '预测订单量',
  `adjust_orders` int(11) NULL DEFAULT NULL COMMENT '手工订单量',
  `true_peoples` int(11) NULL DEFAULT NULL COMMENT '真实客流量',
  `predict_peoples` int(11) NULL DEFAULT NULL COMMENT '预测客流量',
  `adjust_peoples` int(11) NULL DEFAULT NULL COMMENT '手工客流量',
  PRIMARY KEY (`audo_id`) USING BTREE,
  INDEX `idx_cid_goods`(`cid`, `goods_bid`) USING BTREE,
